const Circle = require('../lib/circle.js');

//test to see if square class is a function
describe("shape", () => {
describe("circle", () => {
it("should render a circle shape",() => {
    const circle = new Circle();
const total = `<svg version="1.1" xmlns="http://www.w3.org/2000/svg" width="300" height="200">` + 
`<circle cx="150" cy="100" r="80" fill="${this.color}" />` +
`<text x="50%" y="50%" dominant-baseline="middle" text-anchor="middle" font-size="60" fill="${this.textColor}">` +
`${this.text}</text>` +
`</svg>`
expect(circle.render()).toEqual(total);
});
});
})